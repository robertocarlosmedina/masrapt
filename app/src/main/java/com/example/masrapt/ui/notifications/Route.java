package com.example.masrapt.ui.notifications;

public class Route {

    private int id;
    private String name;
    private String description;
    private int active_bus;
    private int route_timer;
    private String locations;

    public int getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getDescription() {
        return description;
    }

    public int getActive_bus() {
        return active_bus;
    }

    public int getRoute_timer() {
        return route_timer;
    }

    public String getLocations() {
        return locations;
    }
}
